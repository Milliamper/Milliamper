package myPackage;

public class Etterem {
	
	private String nev;
	private String telefonszam;
	private String elso_nyitas;
	private String specialitas;
	
	public Etterem(String nev, String telefonszam, String elso_nyitas, String specialitas) {
		super();
		this.nev = nev;
		this.telefonszam = telefonszam;
		this.elso_nyitas = elso_nyitas;
		this.specialitas = specialitas;
	}

	public String getNev() {
		return nev;
	}

	public String getTelefonszam() {
		return telefonszam;
	}

	public String getElso_nyitas() {
		return elso_nyitas;
	}

	public String getSpecialitas() {
		return specialitas;
	}
	
	
	
	

}
